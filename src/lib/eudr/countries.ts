function norm(s: string): string {
  return s.toLowerCase().normalize('NFD').replace(/[̀-ͯ]/g, '').replace(/[^a-z0-9]/g, '')
}

/** Convertit un pays (nom FR/EN ou code) en code ISO2 ; renvoie l'entrée d'origine en majuscules si inconnu. */
export function toIso2(input: string | null | undefined): string {
  const raw = (input ?? '').trim()
  if (!raw) return ''
  if (/^[A-Za-z]{2}$/.test(raw) && NAME_TO_ISO2[norm(raw)] === undefined) return raw.toUpperCase()
  const hit = NAME_TO_ISO2[norm(raw)]
  return hit ?? raw.toUpperCase()
}
